    public void ValidarPass(String nueva,String conf){
        
        if (nueva.isEmpty() || conf.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese los datos");
        }else{
            if (nueva.equals(conf)) {
                System.out.println("Es Correcto");
                Mensagge("");
            }else{
                Mensagge("Las contraseñas no son iguales");
            }
            Modificar();
        }
    }
    public void Modificar(){
        String nombres = txtNombre.getText(), apellidoPatero = txtApellidoPaterno.getText()
                , apellidoMaterno= txtApellidoMaterno.getText(), User = txtUsuario.getText(), 
                password = txtPassNueva.getText();
        String sql;
        try {
            sql = "update trabajadores set "
                    + "name_usuario='" + User + "',"
                    + "password='" + password + "',"
                    + "nombre='" + nombres + "',"
                    + "Apellido_paterno='" + apellidoPatero+ "',"
                    + "apellido_materno='" + apellidoMaterno+ "',"
                    + "where id='" + idUser + "'";

            conectar.getStatement().executeUpdate(sql);
            dispose();
        } catch (Exception e) {
        }
    }
